                <a name="one"></a><h3>What is 'Manage your collections'?</h3>

                <p>‘Manage your collections’ is a tool that allows archives services to add, edit, delete and restore information about their own collections in The National Archives’ online catalogue, Discovery.</p>
                <p class="back"><a href="">Back to top</a></p>
                <hr>
                <h3 name="two">What is Discovery?</h3>

                <p>Discovery is The National Archives’ online catalogue: visitors can use it to search for information about archival records and identify records that may be relevant to their research. </p>

                <p>Discovery contains more than 32 million descriptions of records held by The National Archives and over 2,500 archives across the country.</p>

                <p>Archives services can use ‘Manage Your Collections’ to upload catalogue information about their own collections to Discovery. We make this information available to Discovery users worldwide.</p>

                <p class="back"><a href="">Back to top</a></p>
                <hr>
                <h3 name="three">What kind of information should be added to Discovery?</h3>

                <p>You can add catalogue data to Discovery in one of two different formats:</p>

                <ul>
                    <li>EAD (Encoded Archival Description): upload data using this international standard for encoding finding aids </li>
                    <li>Excel: upload catalogue data in a Microsoft Excel spreadsheet.</li>
                </ul>
                <p class="back"><a href="">Back to top</a></p>
                <hr>
                <h3 name="four">How will Discovery display your data?</h3>

                <p>Discovery will display this information about your collection:</p>

                <ul>
                    <li>reference number</li>
                    <li>the title of the collection</li>
                    <li>descriptive text</li>
                    <li>the date range of the records in the collection</li>
                </ul>
                <p class="back"><a href="">Back to top</a></p>
                <hr>
                <h3 name="five">Add catalogue descriptions to Discovery</h3>

                <p>Go to the ‘Add a collection’ page where you will be able to upload new catalogue data to Discovery.</p>

                <p>Step 1: download a blank template.</p>

                <p>Choose between EAD and Excel format. Download a blank template and fill in your catalogue data. Do not delete or hide any columns or fields.</p>

                <p>(Please note: we use columns A and B of each template for our own administrative purposes.  These columns are hidden when you download a blank template: column C will be the first column in your template.)</p>

                <p>Save the completed template on your computer.</p>

                <p>Step 2: upload the completed template.</p>

                <p>Click on ‘Upload file’. Select the file you wish to upload by clicking on ‘Choose a file’ in the window that appears. Then click the ‘Upload’ button.</p>

