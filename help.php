                <h4>Preview and approve your catalogue data</h4>

                <p>You will need to preview your catalogue data and approve the way it displays before it can be accepted for publication in Discovery.</p>

                <p>Once your data has been validated, you will see the ‘Preview and approve’ button. Click on this button to see the catalogue data that you have uploaded.</p>

                <p>On the Sample preview page, click on ‘Approve’ or ‘Reject’.</p>

                <p class="back"><a href="">Back to top</a></p>
