                <h3 name="six">Edit catalogue data already held in Discovery</h3>

                <p>Go to the ‘Find a collection’ page. Select or search for the collection you wish to edit.</p>

                <p>To edit data about the collection, click on the ‘Get your collection’ button.  You will need to select whether you want to download your data in EAD or Excel format. </p>

                <p>Then click on ‘Submit’.</p>

                <p>Once you have selected your collection and the appropriate format, you will see the ‘Download template’ button: click on it. This will download a document that you will be able to edit.</p>

                <p>Edit the parts of the collection that you wish to change.  Do not hide or delete any columns.  If there are columns or fields in the template that are not relevant to your cataloguing schema, leave them empty. </p>

                <p>(Please note that we use columns A and B of the template for our own administrative purposes.  These columns will be hidden when you download your collection information.  However, if you are able to see details in columns headed ‘IAID’ and ‘PIAID’, do not edit this information. Making changes in these columns will make it impossible for Discovery to validate your catalogue data.)</p>

                <p>Once you have made the necessary changes, save the document to your computer.</p>

                <p>Within the ‘Edit a collection’ screen, click the ‘Upload changes’ button.</p>

                <p>You will see the the ‘Upload a file’ window: click ‘Choose a file’ and select the file from the location where you have saved it. </p>

                <p>Click the ‘Upload’ button.</p>

                <p class="back"><a href="">Back to top</a></p>
                <hr>

                <h3 name="seven">Delete catalogue data from Discovery</h3>

                <p>Go to the ‘Find a collection’ page. Select or search for the collection you wish to delete.</p>

                <p>Once you have selected the collection you wish to delete you will see the ‘Delete this collection’ button: click on it.</p>

                <p>Select your reason for deletion in the drop-down menu in the window that appears and select ‘Confirm’. </p>

                <p>You should now be able to see the collection awaiting deletion on the ‘View history’ page.</p>

                <p class="back"><a href="">Back to top</a></p>

                <hr>

                <h3 name="eight">Restore catalogue data to Discovery</h3>

                <p>Go to the ‘View history’ page: this lists previous actions performed by your archive.</p>

                <p>Identify the deleted collection that you wish to restore and click on the ‘Restore’ button.</p>

                <p>Please note that deleted collections can only be restored within 7 days of deletion.</p>

                <p class="back"><a href="">Back to top</a></p>

                <hr>

                <h3 name="nine">Troubleshooting</h3>

                <h4>Validating your catalogue data</h4>

                <p>Go to the ‘View history’ page to see the status of your data upload.</p>

                <p>Please note that it will take between several minutes and a few hours to validate your catalogue data, depending on how much you have uploadedlarge your collection is. You will need to periodically refresh this page (click on your web browser’s refresh button or press the F5 key) to see changes in the status of your upload.</p>

                <h4>Upload failed</h4>

                <p>If your catalogue data upload fails, you will see the ‘See error report’ button on the View History page. Click on this for the Validation report, which shows the reason(s) why your data cannot be published in Discovery.</p>

                <p>‘Error’ alerts must be resolved before your catalogue data can be uploaded to Discovery.</p>

                <p>‘Warning’ alerts indicate potential issues that can be resolved to improve the quality of your data as it is displayed in Discovery.</p>

                <p>Each alert indicates the row number where the issue occurs, the column name and details of the problem identified.</p>

                <p>Resolve the errors in the template. Once you have fixed all the errors, repeat the catalogue data upload process.</p>


                <h4>Data format</h4>

                <p>Your catalogue data must be submitted in the correct format for the upload to succeed.  Please download one of our templates – either in EAD or Excel format – and fill in your catalogue data.</p>

                <p>Upload this completed template to add information to Discovery, or to edit information already held in it.</p>

                <p>Do do not delete or hide any columns or fields included in the template. If there are columns or fields in the template that are not relevant to your cataloguing schema, leave them empty.</p>

                <p>We use columns A and B of each template for our own administrative purposes, populating them with unique system identifiers. These columns are hidden when you download a blank template.</p>

                <p>If you are able to see details in columns headed ‘IAID’ and ‘PIAID’, do not edit this information. Making changes in these columns will make it impossible for Discovery to validate your catalogue data.</p>

                <h4>AutoCorrect in Excel</h4>

                <p>Excel's AutoCorrect function may affect some the data that you add to a template.  For example, the AutoCorrect function may automatically alter the surname 'Alway', changing it to 'Always'.</p>

                <p>In Excel, full auto-correction is set as default.</p>

                <p>You may need to disable some of the auto-correction options on any Excel templates that you use for cataloguing projects.</p>

                <p>To do this:</p>

                <ul>
                    <li>Open the spreadsheet you are working on, or the Excel template you intend to distribute to others for cataloguing</li>
                    <li>Click on the ‘File’ menu</li>
                    <li>Select ‘Options’ from the drop-down menu.</li>
                    <li>Click on ‘Proofing’ in the menu on the left side of the dialog box</li>
                    <li>Click the ‘AutoCorrect Options…’ button</li>
                    <li>When the AutoCorrect window appears, uncheck ‘Replace text as you type’ </li>
                    <li>You may also wish to disable 'Correct TWo INitial CApitals', and other options where cataloguing data may include character sequences which Excel does not recognise as correct</li>
                    <li>Click on ‘OK’</li>



